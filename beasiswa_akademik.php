<h1>Beasiswa Akademik</h1>
<p>Beasiswa Akademik tersedia untuk mahasiswa dengan IPK diatas 3.40. Dimana Beasiswa ini akan mencakup biaya kuliah secara penuh dan memberikan tunjangan bulanan untuk biaya hidup sebesar Rp. 1.000.000</p>
<p><strong>Syarat:</strong></p>
<ul>
    <li>Mahasiswa aktif di Universitas Telkom Purwokerto</li>
    <li>Memiliki IPK di atas 3.40.</li>
    <li>Menyerahkan dokumen berupa transkip nilai</li>
</ul>
<p><strong>Cara Mendaftar</strong></p>
<p>Calon penerima beasiswa diwajibkan untuk mengisi formulir registrasi dan melampirkan dokumen berikut:</p>
<ul>
    <li>Transkip Nilai</li>
</ul>
<p><strong>Batas Waktu Pendaftaran:</strong> 31 Desember 2024</p>