<h1>Beasiswa Internal</h1>
<p>Beasiswa Internal tersedia untuk mahasiswa yang merupakan anak dari pegawai Universitas Telkom Purwokerto yang berprestasi dan membutuhkan bantuan biaya kuliah</p>
<p><strong>Syarat:</strong></p>
<ul>
    <li>Mahasiswa anak pegawai aktif di Universitas Telkom Purwokerto</li>
    <li>Memiliki prestasi akademik</li>
    <li>Menyerahkan dokumen berupa bukti status anak pegawai Universitas Telkom Purwokerto</li>
</ul>
<p><strong>Cara Mendaftar</strong></p>
<p>Calon penerima beasiswa diwajibkan untuk mengisi formulir registrasi dan melampirkan dokumen berikut:</p>
<ul>
    <li>Bukti Berupa Kartu Keluarga</li>
</ul>
<p><strong>Batas Waktu Pendaftaran:</strong> 31 Desember 2024</p>