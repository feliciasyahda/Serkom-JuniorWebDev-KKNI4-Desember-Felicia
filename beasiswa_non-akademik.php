<h1>Beasiswa Non-Akademik</h1>
<p>Beasiswa Non-Akademik tersedia untuk mahasiswa dengan prestasi di bidang non-akademik, seperti olahraga, seni, atau bidang lainnya selain akademik</p>
<p><strong>Syarat:</strong></p>
<ul>
    <li>Mahasiswa aktif di Universitas Telkom Purwokerto</li>
    <li>Memiliki prestasi non-akademik sesuai bidang yang dipilih</li>
    <li>Menyerahkan dokumen berupa bukti prestasi seperti sertifikat</li>
</ul>
<p><strong>Cara Mendaftar</strong></p>
<p>Calon penerima beasiswa diwajibkan untuk mengisi formulir registrasi dan melampirkan dokumen berikut:</p>
<ul>
    <li>Bukti Prestasi di Bidang Non-Akademik</li>
</ul>
<p><strong>Batas Waktu Pendaftaran:</strong> 31 Desember 2024</p>